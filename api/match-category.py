class handler(BaseHTTPRequestHandler):
    def do_POST(self):
        try:
            # Read request body
            content_length = int(self.headers['Content-Length'])
            post_data = self.rfile.read(content_length)
            data = json.loads(post_data.decode('utf-8'))
            
            # Get input category
            input_category = data.get("category", "").strip()
            
            if not input_category:
                self.send_error(400, "Category field is required")
                return
            
            # Find best match using fuzzy matching
            match, score, _ = process.extractOne(
                input_category, 
                FACEBOOK_CATEGORIES,
                scorer=fuzz.WRatio  # Best for handling punctuation/case differences
            )
            
            # Determine confidence level
            if score >= 85:
                confidence = "High"
            elif score >= 70:
                confidence = "Medium"  
            else:
                confidence = "Low"
            
            # Prepare response
            response = {
                "original_category": input_category,
                "matched_category": match,
                "confidence_score": score,
                "confidence_level": confidence,
                "needs_manual_review": score < 70,
                "success": True
            }
            
            # Send response
            self.send_response(200)
            self.send_header('Content-type', 'application/json')
            self.send_header('Access-Control-Allow-Origin', '*')
            self.send_header('Access-Control-Allow-Methods', 'POST')
            self.send_header('Access-Control-Allow-Headers', 'Content-Type')
            self.end_headers()
            
            self.wfile.write(json.dumps(response).encode('utf-8'))
            
        except Exception as e:
            # Error response
            error_response = {
                "error": str(e),
                "success": False
            }
            
            self.send_response(500)
            self.send_header('Content-type', 'application/json')
            self.end_headers()
            self.wfile.write(json.dumps(error_response).encode('utf-8'))
    
    def do_OPTIONS(self):
        # Handle preflight requests
        self.send_response(200)
        self.send_header('Access-Control-Allow-Origin', '*')
        self.send_header('Access-Control-Allow-Methods', 'POST')
        self.send_header('Access-Control-Allow-Headers', 'Content-Type')
        self.end_headers()
